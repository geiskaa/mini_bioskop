        String jenis_tiket=jTable1.getValueAt(baris,5).toString();

        // Mengatur tombol yang dipilih dalam grup tombol (buttonGroupjenis)
        if (jenis_tiket.equals("Reguler")){
            rbreguler.setSelected(true);
        }else if(jenis_tiket.equals("Premium")) {
            rbreguler.setSelected(true);
        } else {}

        String harga=jTable1.getValueAt(baris,6).toString();
        txtharga.setText(harga);
