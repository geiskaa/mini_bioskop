    private void txtvoucherActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtvoucherActionPerformed
        // TODO add your handling code here:
        int voucher;
        int subtotal=0;
        int total=0;
        String bonus = "";
        
        try{
            voucher = Integer.parseInt (txtvoucher.getText());
            total = subtotal-voucher;
            txttotal.setText(String.valueOf(total));
        }catch (NumberFormatException ex){
            JOptionPane.showMessageDialog(null,"Nilai Harus Berupa Angka");
        }
        
        if (total >= 300000){
            bonus = "Marchandise";
        }else if (total >= 200000){
            bonus = "PopCorn";
        }else if (total >= 100000){
            bonus = "Voucher Rp5000";
        }else {
            JOptionPane.showMessageDialog(null, "Bonus Tidak Tersesedia");
        }  
        txtbonus.setText (bonus);
 
    }//GEN-LAST:event_txtvoucherActionPerformed

